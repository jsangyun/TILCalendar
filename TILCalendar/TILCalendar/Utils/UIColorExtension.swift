import Foundation
import UIKit

extension UIColor {
    
    static var scarlet: UIColor {
        UIColor(red: 0.9451, green: 0.6118, blue: 0.4745, alpha: 1)
    }
    
    static var brightGreen: UIColor {
        UIColor(red: 0.7961, green: 0.8745, blue: 0.7412, alpha: 1)
    }
    
    static var skyBlue: UIColor {
        UIColor(red: 0.0392, green: 0.8275, blue: 1, alpha: 1)
    }
    
    static var blackColor: UIColor {
        .black.withAlphaComponent(0.6)
    }
}
